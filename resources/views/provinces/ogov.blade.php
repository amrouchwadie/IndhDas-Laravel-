                - Planification à travers la consolidation et validation des PPDH provinciaux et préfectoraux en assurant la convergence avec les différentes parties prenantes. <br>
                - Répartition des ressources financières selon les PPDH adoptés ; <br>
                - Arbitrage entre les PPDH en assurant la convergence avec les services extérieurs de la région;<br>
                - Suivi de la mise en œuvre des PPDH ;<br>
                - Capitalisation et échange des bonnes pratiques et des expériences réussies.<br><br>

         
