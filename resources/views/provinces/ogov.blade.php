           <b>     Missions :</b><br><br>
                Élaboration et actualisation du diagnostic participatif, en tenant compte des documents de planification existants ;<br>
                Suivi des projets retenus et leur concrétisation sur le terrain ;<br>
                Pérennisation des projets existants ;<br>
                Suivi des indicateurs en relation avec le développement humain au niveau de la circonscription.<br>
                Echange d’expériences et des bonnes pratiques.<br>
                Sensibilisation et diffusion des valeurs de l’INDH ;<br>
