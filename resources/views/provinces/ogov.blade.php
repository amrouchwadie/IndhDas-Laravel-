                La Division de l’Action Sociale (DAS) de la région assurera le secrétariat du CRDH.<br><br>
                
               <b> Outil contractuel</b><br>
                Une Contractualisation entre CPDH et CRDH doit être conclue pour fixer les objectifs et les engagements de tous les partenaires.<br>
