                <br> <br>
               <b> Missions :</b> <br>
                - Analyser et valider les diagnostics participatifs émanant des CLDH en veillant à leur convergence et complémentarité avec les programmes sectoriels ; <br>
                - Élaborer le programme pluriannuel de développement humain de la province (PPDH) ; <br>
                - Exécuter et mettre en œuvre le PPDH, en apportant les réajustements nécessaires dans le cas échéant ; <br>
                - Elaborer et mettre en œuvre les plans territoriaux de renforcement des capacités et de communication de proximité ; <br>
                - Elaborer les conventions spécifiques avec les associations têtes de réseau ; <br>
                - Assurer l’entretien et la maintenance des infrastructures réalisées et équipements acquis ; <br>
                - Veiller au reporting des indicateurs de suivi des réalisations et de l’évolution des indicateurs de développement humain. <br> <br>
               <b> Organisation :</b> <br> <br>
                
                Le CPDH est créé par un arrêté gubernatorial, présidé par le Gouverneur de la province/préfecture, et composé de 30 membres au maximum, dont au moins 20% de femmes et 15% de jeunes, répartis ainsi : <br> <br>
                
                - Elus : Président du Conseil provincial/ préfectoral, Président de la commune chef-lieu, président de la commission provinciale de développement économique sociale et culturel et chefs des groupes politiques formant le conseil afin d’assurer la représentativité de tous les partis politiques ; <br>
                - Secrétaire général de la préfecture/province; <br>
                - Directeurs préfectoraux/provinciaux des services déconcentrés de l’Etat concernés ; <br>
                - Représentants de la société civile : têtes de réseau et les associations locales particulièrement celles des femmes et des jeunes ; <br>
                - Personnes ressources cooptées par le CPDH : Mécènes, bienfaiteurs, intellectuels, secteur privé, etc. <br> <br>
            <b>   Fonctionnement du CPDH :</b>  <br> <br>
                Le CPDH doit être doté d’un règlement intérieur qui précisera les modalités de son fonctionnement et de prise de décision, le processus de provocation des réunions et le quorum. <br> <br> 
                
                La Division de l’Action Sociale assurera le secrétariat du CPDH. <br> <br>
                
              <b>  Outil de planification :</b> <br> <br>
                
                Le Programme Pluriannuel de Développement Humain (PPDH) permet d’élaborer une vision globale et intégrée du développement humain au niveau provincial/ préfectoral <br>
